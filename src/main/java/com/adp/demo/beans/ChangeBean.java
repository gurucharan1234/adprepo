package com.adp.demo.beans;

public class ChangeBean {
	
	private Integer quarters;
	private Integer dimes;
	private Integer nickels;
	private Integer pennys;
	
	/**
	 * @return the quarters
	 */
	public Integer getQuarters() {
		return quarters;
	}
	/**
	 * @param quarters the quarters to set
	 */
	public void setQuarters(Integer quarters) {
		this.quarters = quarters;
	}
	/**
	 * @return the dimes
	 */
	public Integer getDimes() {
		return dimes;
	}
	/**
	 * @param dimes the dimes to set
	 */
	public void setDimes(Integer dimes) {
		this.dimes = dimes;
	}
	/**
	 * @return the nickels
	 */
	public Integer getNickels() {
		return nickels;
	}
	/**
	 * @param nickels the nickels to set
	 */
	public void setNickels(Integer nickels) {
		this.nickels = nickels;
	}
	/**
	 * @return the pennys
	 */
	public Integer getPennys() {
		return pennys;
	}
	/**
	 * @param pennys the pennys to set
	 */
	public void setPennys(Integer pennys) {
		this.pennys = pennys;
	}
	
}
